await mkdir(dirname(outputPath), { recursive: true })
await writeFile(outputPath, `${banner}${JSON.stringify(normalized)}\n`, 'utf8')
console.info(`Wrote ${normalized.length} ScreenRes display profiles to ${outputPath}`)
